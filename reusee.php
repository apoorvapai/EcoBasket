4.How can I reuse waste?<br>
*Newspapers<br>
The management of old news papers is a big tussles and we need to think about its effective reusability. The newspaper can be used to replace plastic bags to pack small groceries in the grocery shops and this will help to reuse newspaper as well as avoid the threat of increasing plastic bag pollution. The another way is to convert the newspaper into new products like door mats, table mats etc.<br>
*Plastic bags<br>
Plastic bags are popular because it is cheap and easy to use. Due to this the volume of plastic bag waste is huge. Most of the plastic bags never biodegrade and it will create huge pollution. The only way to control the waste is reuse the plastic bags again and again. The shops should insist the customers to carry their own bags for getting additional discounts and they should charge higher amount for each new bag used by the customer. This will force the customers to reuse the plastic bags again and again.<br>
*Electronics<br>
Electronic wastage is going to be one of the biggest wastes. The only method to reduce this is reusing the electronic equipment by donating it to someone who is willing to use it further. For printer ink cartridges refill the ink and reuse it again and again. This will reduce the cost of the print and reduce the waste.<br>
*Tins and cans<br>
Tins and cans can be effectively reused, by way of making various by products from this. The pull tabs for can which we used to throw to the trash bin, can be used to make various products like hand bags, hooks etc. The other way to reuse the can and tins is to make furniture for your garden or other open spaces. Since the furniture is made out of metal the life time will be longer even though if you put this outside your home.<br>
*Plastic bottles<br>
Plastic waste handling is one of the toughest tasks. Plastic bottles can be reused in many ways. The bottom of plastic bottle can be reused as funnel and the bottom part can be used for cultivating small plants.<br>
<br>
